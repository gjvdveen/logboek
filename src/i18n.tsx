// ── Context ──────────────────────────────────────────────────────────────────
type TFn = (key: keyof typeof nl, ...args: (string | number)[]) => string;

interface LangContextValue { lang: Lang; setLang: (l: Lang) => void; t: TFn; }

const LangContext = createContext<LangContextValue>({
  lang: 'nl',
  setLang: () => {},
  t: (key) => key,
});

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const stored = (localStorage.getItem('lang') as Lang) ?? 'nl';
  const [lang, setLangState] = useState<Lang>(stored);

  function setLang(l: Lang) {
    localStorage.setItem('lang', l);
    setLangState(l);
  }

  const dict = lang === 'nl' ? nl : en;
  const t: TFn = (key, ...args) => {
    let s: string = dict[key] ?? nl[key] ?? key;
    args.forEach((a, i) => { s = s.replace(`{${i}}`, String(a)); });
    return s;
  };

  return <LangContext.Provider value={{ lang, setLang, t }}>{children}</LangContext.Provider>;
}

export function useTranslation() {
  return useContext(LangContext);
}
